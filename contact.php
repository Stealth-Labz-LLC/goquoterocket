            function feedbackMail(formData) {
                $('#loading-indicator').show();
                let queryStringValue = window.location.search;
                $.ajax({
                    url: 'https://quoterocket.co.za/mail-action.php?' + formData,
                    // Type of Request
                    type: 'post',
                    success: function(data) {
                        let resultData = JSON.parse(data);
                        console.log(resultData)

                        if (resultData.code === 1) {
                            $('#loading-indicator').hide();
                            $('#feedback_response_body').show();
                            $('#feedback_response_body p').text(resultData.status_message);
                            $("#feedback_form1")[0].reset();
                        } else {
                            $('#loading-indicator').hide();
                            $('#feedback_response_body').show();
                            $('#feedback_response_body p').text(resultData.status_message);
                            $("#feedback_form1")[0].reset();
                        }
                    },
                    // Error handling
                    error: function(error) {
                        $('#loading-indicator').hide();
                        console.log(`Error ${error}`);
                    }
                });
            }
